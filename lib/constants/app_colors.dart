import 'package:flutter/material.dart';

/// Centralized color theme for the fitness app
class AppColors {
  // Private constructor to prevent instantiation
  AppColors._();

  /// Primary dark background color
  static const Color primary = Color(0xFF1A1A2E);

  /// Accent color for call-to-action elements
  static const Color accent = Color(0xFFFF6B35);

  /// Secondary color for alternative selections
  static const Color secondary = Color(0xFF004E89);

  /// Success/positive action color
  static const Color success = Color(0xFF1EBE73);

  /// Warning/caution color
  static const Color warning = Color(0xFFFFA500);

  /// White text color
  static const Color textWhite = Colors.white;

  /// Muted white text color
  static const Color textMuted = Colors.white70;
}
